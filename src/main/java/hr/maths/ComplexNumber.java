package hr.maths;

public class ComplexNumber {

	private double real;
	private double imag;
	
	public ComplexNumber() {
		this.real = 0;
		this.imag = 0;
	}

	public double getReal() {
		return real;
	}

	public void setReal(double real) {
		this.real = real;
	}

	public double getImag() {
		return imag;
	}

	public void setImag(double imag) {
		this.imag = imag;
	}
	
	
}
